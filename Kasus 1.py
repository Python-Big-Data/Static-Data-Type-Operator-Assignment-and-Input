# Susunlah kode Python sedemikian hingga dapat digunakan untuk menghitung total luas segitiga sebanyak 123 buah dengan
# setiap segitiganya berukuran panjang alas 30 satuan dan tinggi 18 satuan

# Diketahu bahwa panjang alas 30 satuan

alas = 30

# Diketahu bahwa tinggi 18 satuan

tinggi = 18

# Sekarang kita cara luas satu buah segitiga

luasSegitiga = (alas * tinggi)/2

# Lalu print luas sigitiga di atas dengan mengalikannya dengan 123 buah

luasTotal = str(luasSegitiga*123)

print('Luas total segitiga adalah ' + luasTotal + ' satuan luas')


